package com.huonu.domain.model;

import java.util.Date;

public class TaobaoZsCatEntry {
	
	private String taobao_user_id;
	private Long cat_id;
	private Long campaign_type;
	private String cat_name;
	private String option_name;
	private String option_value;
	private Date last_update_time;
	
	public String getTaobao_user_id() {
		return taobao_user_id;
	}
	public void setTaobao_user_id(String taobao_user_id) {
		this.taobao_user_id = taobao_user_id;
	}
	
	public Long getCampaign_type() {
		return campaign_type;
	}
	public void setCampaign_type(Long campaign_type) {
		this.campaign_type = campaign_type;
	}
	
	public String getOption_name() {
		return option_name;
	}
	public void setOption_name(String option_name) {
		this.option_name = option_name;
	}
	public String getOption_value() {
		return option_value;
	}
	public void setOption_value(String option_value) {
		this.option_value = option_value;
	}
	public Date getLast_update_time() {
		return last_update_time;
	}
	public void setLast_update_time(Date last_update_time) {
		this.last_update_time = last_update_time;
	}
	public Long getCat_id() {
		return cat_id;
	}
	public void setCat_id(Long cat_id) {
		this.cat_id = cat_id;
	}
	public String getCat_name() {
		return cat_name;
	}
	public void setCat_name(String cat_name) {
		this.cat_name = cat_name;
	}

	
}
